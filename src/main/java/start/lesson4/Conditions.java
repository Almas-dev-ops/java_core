package start.lesson4;

public class Conditions {
    public static void main(String[] args) {
        int x = 15;
        int y = 10;

        if (x > y){
            System.out.println("X больше чем Y");
        } else if (x < y) {
            System.out.println("X меньше чем Y");
        } else if (x == y) {
            System.out.println("X и Y равны.");
        }

    }
}
